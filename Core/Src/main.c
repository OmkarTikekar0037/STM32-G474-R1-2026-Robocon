/* USER CODE BEGIN 0 */
void recombination(uint8_t *p){
	ry = (uint16_t)p[0]-128;
	rx = (uint16_t)p[1]-128;
	lx = (uint16_t)p[2]-128;
	uart_line_ready = 1;
	Timeout = HAL_GetTick();
}

void UART_receive(uint8_t byte){
	switch(state){
	case HEADER_1 :
		if(byte== 0x32) state = HEADER_2;

		break;

	case HEADER_2 :
		if(byte== 0x32){
			state = DATA;
			idx=0;
			checksum=0;
		}
		else {
			state = HEADER_1;
		}

		break;

	case DATA :
		data[idx++] = byte;
		checksum = (checksum + byte) & 0xFF;
			if(idx>=3){
				state = CHECKSUM;
			}
		break;
	case CHECKSUM:
		if(byte == checksum){
			recombination(data);
		}
		state = HEADER_1;
		break;

	}

}
